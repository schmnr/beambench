/** Return '#fff' or '#000' for best contrast against a hex background. */
function getContrastText(hex: string): string {
  const h = hex.replace('#', '');
  const r = parseInt(h.substring(0, 2), 16);
  const g = parseInt(h.substring(2, 4), 16);
  const b = parseInt(h.substring(4, 6), 16);
  return (r * 299 + g * 587 + b * 114) / 1000 > 128 ? '#000' : '#fff';
}

/** Get the palette index number for the layer chip display. */
function getPaletteIndex(layer: Layer): string {
  const sourceIndex = lbrnPaletteIndex(layer);
  if (sourceIndex !== null) {
    return sourceIndex >= 30 ? `T${sourceIndex - 29}` : String(sourceIndex).padStart(2, '0');
  }

  const colorTag = layer.color_tag;
  const norm = normalizeHex(colorTag);
  const idx = PALETTE_COLORS.findIndex(
    (c) => normalizeHex(c.hex) === norm,
  );
  if (idx >= 0) {
    const entry = PALETTE_COLORS[idx];
    if (entry.is_tool_layer) return `T${idx - 29}`;
    return String(idx).padStart(2, '0');
  }
  return '??';
}

