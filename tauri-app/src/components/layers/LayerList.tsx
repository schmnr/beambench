/** Matches auto-generated family labels like "C00", "C01", "T1", "T2". */
const AUTO_FAMILY_LABEL_RE = /^[CT]\d+$/;

/** Matches auto-generated sibling names like "C02 (Image)", "T1 (Line)". */
const AUTO_SIBLING_NAME_RE = /^[CT]\d+\s+\((Image|Line|Cut|Score|Fill|Offset Fill)\)$/;

function isAutoGeneratedFamilyName(layer: Layer): boolean {
  const name = layer.name?.trim();
  if (!name) return true;
  if (DEFAULT_LAYER_NAMES.has(name)) return true;
  // Match current family label or any stale palette-style label (C##, T#).
  // This catches names like "C02 (Image)" that became stale after recolor.
  if (AUTO_FAMILY_LABEL_RE.test(name)) return true;
  if (AUTO_SIBLING_NAME_RE.test(name)) return true;
  const familyLabel = getLayerLabel(layer);
  if (name === familyLabel) return true;
  return name === `${familyLabel} (${operationDisplayLabel(layerOperation(layer))})`;
}

function displayLayerName(layer: Layer): string {
  return isAutoGeneratedFamilyName(layer)
    ? getLayerLabel(layer)
    : layer.name;
}

