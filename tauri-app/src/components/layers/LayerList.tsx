/** Names the backend assigns by default — treat these as "no custom name". */
const DEFAULT_LAYER_NAMES = new Set(['Line', 'Fill', 'Offset Fill', 'Image', 'Cut', 'Score']);

/** Normalize color tag for comparison — strip alpha suffix, lowercase. */
function normalizeHex(hex: string): string {
  let h = hex.toLowerCase().trim();
  // Strip 8-char RGBA to 6-char RGB (e.g. #ff0000ff → #ff0000)
  if (h.length === 9 && h.startsWith('#')) h = h.slice(0, 7);
  return h;
}

function autoFamilyLabel(name: string | undefined): string | null {
  const match = name?.trim().match(/^([CT]\d+)(?:\s+\((?:Image|Line|Cut|Score|Fill|Offset Fill)\))?$/);
  return match?.[1] ?? null;
}

function lbrnPaletteIndex(layer: Layer): number | null {
  const label = autoFamilyLabel(layer.name);
  if (!label) return null;
  const index = label.startsWith('T')
    ? Number(label.slice(1)) + 29
    : Number(label.slice(1));
  if (!Number.isInteger(index) || index < 0 || index >= LBRN_PALETTE_COLORS.length) {
    return null;
  }
  return normalizeHex(LBRN_PALETTE_COLORS[index]) === normalizeHex(layer.color_tag)
    ? index
    : null;
}

function paletteLabel(index: number): string {
  return index >= 30 ? `T${index - 29}` : `C${String(index).padStart(2, '0')}`;
}

function getLayerLabel(layer: Layer): string {
  const sourceIndex = lbrnPaletteIndex(layer);
  if (sourceIndex !== null) return paletteLabel(sourceIndex);

  const colorTag = layer.color_tag;
  const norm = normalizeHex(colorTag);
  const idx = PALETTE_COLORS.findIndex(
    (c) => normalizeHex(c.hex) === norm,
  );
  if (idx >= 0) {
    return paletteLabel(idx);
  }
  // Fallback: use order_index
  return `C${String(layer.order_index).padStart(2, '0')}`;
}

function operationDisplayLabel(op: OperationType): string {
  switch (op) {
    case 'tool': return 'Tool';
    case 'image': return 'Image';
    case 'line': return 'Line';
    case 'cut': return 'Cut';
    case 'score': return 'Score';
    case 'fill': return 'Fill';
    case 'offset_fill': return 'Offset Fill';
  }
}

